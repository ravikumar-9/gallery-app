class Gallery extends Component {
  state = {activeImageId: imagesList[0].id}

  getFilteredImages = () => {
    const {activeImageId} = this.state

    const filteredImages = imagesList.filter(
      eachImage => eachImage.id === activeImageId,
    )

    return filteredImages
  }

  updateImageId = id => {
    this.setState({activeImageId: id})
  }

  render() {
    const filteredImagesList = this.getFilteredImages()

    const {activeImageId} = this.state

    return (
      <div className="bg-container">
        <ul className="img-container">
          {filteredImagesList.map(eachImage => (
            <img
              src={eachImage.imageUrl}
              alt={eachImage.imageAltText}
              className="images"
            />
          ))}
        </ul>
        <h1 className="nature-heading">Nature Photography</h1>
        <p className="description">Nature Photography by Rahul</p>
        <ul className="thumbnail-container">
          {imagesList.map(eachThumbnail => (
            <ThumbnailItem
              thumbnailDetails={eachThumbnail}
              updateImageId={this.updateImageId}
              key={eachThumbnail.id}
              isActiveImage={activeImageId === eachThumbnail.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
